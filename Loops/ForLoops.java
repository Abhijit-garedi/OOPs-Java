package Loops; // This is the package name as per the folder structure.

public class ForLoops {
    public static void main(String[] args) {

        // For loop to print "Hello World" 5 times
        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello World");
        }
    }
}


